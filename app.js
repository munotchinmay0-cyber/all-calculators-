function triggerCalculation(inputId) {
  // Figure out which calculator view this input belongs to
  if (inputId.startsWith('sip-')) {
    runCalculator('sip');
  } else if (inputId.startsWith('swp-')) {
    runCalculator('swp');
  } else if (inputId.startsWith('lump-')) {
    runCalculator('lumpsum');
  } else if (inputId.startsWith('loan-')) {
    runCalculator('loan');
  } else if (inputId.startsWith('goal-')) {
    runCalculator('goal');
  }
}

/**
 * 3. CORE CALCULATOR ROUTER
 */
function initCalculators() {
  // Run all calculations once at startup
  runCalculator('sip');
  runCalculator('swp');
  runCalculator('lumpsum');
  runCalculator('loan');
  runCalculator('goal');
}

function runCalculator(id) {
  switch (id) {
    case 'sip':
      calcSIP();
      break;
    case 'swp':
      calcSWP();
      break;
    case 'lumpsum':
      calcLumpsum();
      break;
    case 'loan':
      calcLoan();
      break;
    case 'goal':
      calcGoal();
      break;
  }
}

