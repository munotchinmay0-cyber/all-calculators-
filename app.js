/**
 * 1. NAVIGATION & TAB SWITCHING
 */
function initNavigation() {
  const navItems = document.querySelectorAll('.nav-item');
  const views = document.querySelectorAll('.calculator-view');
  const currentTitle = document.getElementById('current-calc-title');
  const currentDesc = document.getElementById('current-calc-desc');

  const calcMetadata = {
    sip: {
      title: 'Systematic Investment Plan (SIP) Calculator',
      desc: 'Estimate the wealth generated by your monthly mutual fund or stock investments over time.'
    },
    swp: {
      title: 'Systematic Withdrawal Plan (SWP) Calculator',
      desc: 'Calculate how long your retirement corpus will last with recurring monthly withdrawals, taking inflation and growth into account.'
    },
    lumpsum: {
      title: 'Lumpsum Investment Calculator',
      desc: 'Calculate the future value of your one-time investments with nominal and inflation-adjusted projections.'
    },
    loan: {
      title: 'Loan & Education Loan EMI Calculator',
      desc: 'Calculate your EMI, analyze the amortization schedule, explore prepayments, and factor in student loan moratoriums.'
    },
    goal: {
      title: 'Goal Planner & Retirement Calculator',
      desc: 'Find out exactly how much you need to save monthly to achieve your future financial targets, adjusted for inflation.'
    }
  };

  navItems.forEach(item => {
    const button = item.querySelector('button');
    button.addEventListener('click', () => {
      const targetViewId = button.getAttribute('data-target');
      
      // Update active nav class
      navItems.forEach(nav => nav.classList.remove('active'));
      item.classList.add('active');

      // Update views visibility
      views.forEach(view => {
        if (view.id === targetViewId) {
          view.classList.add('active');
        } else {
          view.classList.remove('active');
        }
      });

      // Update headers
      const metadata = calcMetadata[targetViewId];
      if (metadata) {
        currentTitle.textContent = metadata.title;
        currentDesc.textContent = metadata.desc;
      }

      // Execute calculation for newly selected calculator
      runCalculator(targetViewId);
    });
  });
}

