/**
 * E. Goal Planner & Retirement Calculator
 */
function calcGoal() {
  const targetCorpus = parseFloat(document.getElementById('goal-target').value) || 0;
  const years = parseFloat(document.getElementById('goal-years').value) || 0;
  const annualRate = parseFloat(document.getElementById('goal-rate').value) || 0;
  
  const inflationToggle = document.getElementById('goal-inflation-toggle').checked;
  const inflationRate = inflationToggle ? parseFloat(document.getElementById('goal-inflation').value) || 0 : 0;

  const stepUpToggle = document.getElementById('goal-stepup-toggle').checked;
  const stepUpRate = stepUpToggle ? parseFloat(document.getElementById('goal-stepup').value) || 0 : 0;

  const monthlyRate = annualRate / 12 / 100;
  const totalMonths = years * 12;

  // 1. Calculate future inflated target corpus
  const inflationAdjustmentFactor = Math.pow(1 + inflationRate / 100, years);
  const inflatedTarget = targetCorpus * inflationAdjustmentFactor;

  // 2. Solve for starting Monthly SIP using simulation / numeric matching (since step-up makes mathematical formulas complex)
  // Let's use simple secant method/binary search to solve for 'P' (initial monthly SIP)
  // which satisfies: FutureValue(P) = inflatedTarget
  
  let low = 0;
  let high = inflatedTarget;
  let startingSIP = 0;
  let achievedFV = 0;
  let totalInvested = 0;

  // Binary search for exact starting monthly SIP
  for (let iter = 0; iter < 50; iter++) {
    startingSIP = (low + high) / 2;
    
    // Simulate SIP build up
    let tempFV = 0;
    let tempInvested = 0;
    let tempSIP = startingSIP;

    for (let m = 1; m <= totalMonths; m++) {
      if (stepUpToggle && m > 1 && (m - 1) % 12 === 0) {
        tempSIP = tempSIP * (1 + stepUpRate / 100);
      }
      tempInvested += tempSIP;
      tempFV = (tempFV + tempSIP) * (1 + monthlyRate);
    }

    achievedFV = tempFV;
    totalInvested = tempInvested;

    if (Math.abs(achievedFV - inflatedTarget) < 1) {
      break;
    } else if (achievedFV < inflatedTarget) {
      low = startingSIP;
    } else {
      high = startingSIP;
    }
  }

  const interestEarned = Math.max(0, inflatedTarget - totalInvested);

  // Update UI values
  document.getElementById('goal-kpi-sip').textContent = formatCurrency(startingSIP);
  document.getElementById('goal-kpi-inflated').textContent = formatCurrency(inflatedTarget);
  document.getElementById('goal-kpi-invested').textContent = formatCurrency(totalInvested);
  document.getElementById('goal-kpi-earnings').textContent = formatCurrency(interestEarned);

  // Display inflation alert
  const goalAlertContainer = document.getElementById('goal-alert-container');
  if (inflationToggle && inflationRate > 0) {
    goalAlertContainer.innerHTML = `
      <div class="alert alert-warning">
        <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor">
          <path stroke-linecap="round" stroke-linejoin="round" d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-3L13.732 4c-.77-1.333-2.694-1.333-3.464 0L3.34 16c-.77 1.333.192 3 1.732 3z" />
        </svg>
        <div>
          <div class="alert-title">Inflation Impact Alert</div>
          <div class="alert-description">Due to <strong>${inflationRate}%</strong> inflation, the target corpus value of <strong>${formatCurrency(targetCorpus)}</strong> today increases to <strong>${formatCurrency(inflatedTarget)}</strong> in ${years} years to retain equivalent purchasing power.</div>
        </div>
      </div>
    `;
  } else {
    goalAlertContainer.innerHTML = '';
  }

  // Draw Chart
  renderDoughnutChart('goal-chart', ['Total Invested', 'Growth Needed'], [totalInvested, interestEarned], ['#818cf8', '#10b981']);

  // Render Table
  let tableHtml = `
    <table>
      <thead>
        <tr>
          <th>Year</th>
          <th>Monthly SIP</th>
          <th>Total Invested</th>
          <th>Target Growth Progress</th>
          <th>Target Gap Remaining</th>
        </tr>
      </thead>
      <tbody>
  `;

  let currentSIP = startingSIP;
  let cumInvested = 0;
  let tempFV = 0;

  for (let y = 1; y <= years; y++) {
    for (let m = 1; m <= 12; m++) {
      cumInvested += currentSIP;
      tempFV = (tempFV + currentSIP) * (1 + monthlyRate);
    }

    const gap = Math.max(0, inflatedTarget - tempFV);

    tableHtml += `
      <tr>
        <td>Year ${y}</td>
        <td>${formatCurrency(currentSIP)}</td>
        <td>${formatCurrency(cumInvested)}</td>
        <td>${formatCurrency(tempFV)}</td>
        <td>${gap > 0 ? formatCurrency(gap) : 'Goal Achieved!'}</td>
      </tr>
    `;

    if (stepUpToggle) {
      currentSIP = currentSIP * (1 + stepUpRate / 100);
    }
  }

  tableHtml += `</tbody></table>`;
  document.getElementById('goal-table').innerHTML = tableHtml;
}

